// armazendo uma função em uma variavel

const imprimirSoma = function (a,b){
    console.log (a+b)
}
imprimirSoma(2,3);

//armazendo uma função arrow em uma varivel
const soma = (a,b) => {
    return a+b
}
console.log (soma (2,2))

const subtraçao = (a,b) => a - b

